<p>The Innovation and Entrepreneurship Development Centres (IEDC) are the entities set up in
Engineering, Management, Arts & Science Colleges and
Polytechnics with an aim to provide an exciting platform for experimentation and
innovation. Kerala Startup Mission has set up IEDCs in 214
Institutions across the State which provide avenues for creative students to transform
their innovative ideas into viable products and services.
IEDCs works as the first launch pad for a student’s entrepreneurial journey and provide
them with access to cutting edge technology, world class
infrastructure, high quality mentorship, early risk capital and global exposure.
<br>IEDC Summit is the avenue for participants of various units across the State to come
together, to share the success stories, to learn from each
other’s failure, to show case innovative products and to develop strong networks that go a
long way in developing sustainable enterprises.<br>
This year's IEDC Summit is going to be conducted on <strong>3 November 2018</strong> in the
picturesque campus of <strong>Amal Jyothi College of
Engineering</strong>, Kanjirappally. We invite you to be part of the best IEDC Summit
you have ever witnessed.</p>
